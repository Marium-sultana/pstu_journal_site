      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      After submission of a manuscript, it will be first checked by the editorial team members whether the journal
      guidelines were followed and it passed through similarity index of less than 20%. Next, the manuscript will 
      be evaluated by two competent reviewers following a double blind review process. Authors are encouraged to 
      provide the names of three potential reviewers. After review, one of the associate editor will provide a 
      decision on the submission. If revisions are suggested, the authors need to provide a rebuttal to the reviews’ 
      comments while a fresh and track-changed version need to be submitted.  If the manuscript is rejected at any 
      stage of the review process, the authors can appeal to the editor in chief for reconsideration of the
      manuscript with explaining why they disagree with the decision.   <o:p></o:p>
      </span>
      
</p>

