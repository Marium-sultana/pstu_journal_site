<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;line-height:normal'>
      <b>
            <h5 style="color:	 #000080"> PREPARATION OF MANUSCRIPT </h5>
            <span style='font-size:12.0pt;font-family:"Times New Roman","serif";
                  mso-fareast-font-family:"Times New Roman";color:#002060'>
                  Format 
            </span>
      </b>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:
            "Times New Roman";color:#002060'><o:p></o:p>
      </span>
</p>

<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      The manuscript should be typed in Times New Roman font and size is 12 (twelve) in double-space with margins of at 
      least 2.5 cm at top, bottom and both sides on paper while a continuous number for all lines throughout the manuscript 
      should be used. A manuscript should be organized as follows-<o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      a)	A cover letter- Please write why and how your manuscript is novel and important for our readers. </br>
      b)    Title page- title of the study with contributors and their affiliations. Please include the email address and ORCID ID (if available) of the corresponding authors.</br>
      c)	The manuscript-   Please include all your tables and figures separately.</br>
<o:p></o:p>
      </span>
      
</p>

<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;line-height:normal'>
      <b>
            <span style='font-size:12.0pt;font-family:"Times New Roman","serif";
                  mso-fareast-font-family:"Times New Roman";color:#002060'>
                  Full Length Paper 
            </span>
      </b>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:
            "Times New Roman";color:#002060'><o:p></o:p>
      </span>
</p>

<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      A full length manuscript except review article and short communications should contain the following parts:  <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
            <b>Title:</b> The title of the paper should be brief but should reflect the content of the work.
                  Include the name of the organism used if appropriate. Use the common names of plants 
                  except for little-known species and weeds. The first letter of all words should 
                  capitalize except articles, prepositions and conjunctions. 
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Abstract:</b>The abstract should be informative and completely self-explanatory. 
      It should be written as one paragraph with not exceeding 250 words. 
      The abstract should clearly represent the rationale of the study 
      with its objectives, materials and techniques used, results and conclusions.
      Paragraphs, footnotes, references, cross-references to figures and tables
      and undefined abbreviations should be avoided. Acronyms should be avoided if possible. 
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Key words:</b> Five or less than five key words should be included after the abstract. 
      However, these keywords cannot be same of the title of the study while they should be arranged alphabetically. 
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Introduction:</b> The introduction should state clearly the rationale for conducting 
      the research stating the problem, justifying the research and the findings of earlier research and the objectives of the study. 
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Materials and Methods/Methodology:</b> Materials and methods should be sufficiently described in the 
      manuscript so that the work may be evaluated or repeated by other researchers. Established procedures and 
      tests should not be described in detail but can be referred with appropriate citations. 
      Chemical procedures that are referenced should include a brief statement of the basic principles of the method.  
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Results and Discussion:</b>  Results and Discussion may be presented as separate sections or can be combined 
      under one heading. However, separate presentation of results and discussion is preferable. The authors should 
      explain their findings in the Discussion section with attribution of the possible mechanisms. The discussion 
      should be supported with relate previous finding. 
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Conclusion:</b> In conclusions, authors should summarize their major findings and state inferences of these 
      results in studied field. Please be aware that conclusions should be presented as a standalone section so that
      readers do not need to go through other sections 
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Acknowledgements (if any):</b> The authors may acknowledge the contribution of other persons and organization.
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>Declaration of funding:</b> The authors should clearly declare if they have received any funding from an organization. Please include the fund number.
      <o:p></o:p>
      </span>
      
</p>
<p class=MsoNormal style='mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;
      text-align:justify;text-justify:inter-ideograph;line-height:normal'>
      <span style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>
      <b>References:</b> The journal uses the name-and-year system (Harvard system) in which the author's(s') name(s)
      followed by the year of publication is cited in the text. For instance, in text citation should be made as (Rahman, 2020) 
      or (Abdul et al., 2020; Rahman et al. 2020) at the end of any statement while Rahman (2020) or Abdul et al. (2020) format 
      should be used when citations are included as a part of the sentence. 
      <o:p></o:p>
      </span>
      
</p>

